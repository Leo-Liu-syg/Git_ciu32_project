/*------------------------------------------includes--------------------------------------------*/
#include "main.h"

/*-------------------------------------------functions------------------------------------------*/
int main(void)
{
    /* 配置系统时钟 */
    system_clock_config();
  
    /* GPIO初始化 */
    gpio_init();
    
    /* SPI初始化，配置为从模式 */
    spi1_init();
    
    /* NVIC初始化 */
    nvic_init();
    
    /* 等待发送完成 */
    while(g_send_count < 8);
    
    /* 等待接收完成 */
    while(g_recv_count < 8);
    
    while(1)
    {
    }
}












